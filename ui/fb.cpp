Framebuffer::Framebuffer()
    : buffer_(nullptr)
    , dirty_(true) {
    buffer_ = g_framebuffer_pixels;
    clear(Color::Black());
}

Framebuffer::~Framebuffer() {
    buffer_ = nullptr;
}

void Framebuffer::clear(Color c) {
    uint32_t pixel = (c.a << 24) | (c.r << 16) | (c.g << 8) | c.b;
    for (size_t i = 0; i < FB_WIDTH * FB_HEIGHT; ++i) {
        buffer_[i] = pixel;
    }
    dirty_ = true;
}

void Framebuffer::set_pixel(int32_t x, int32_t y, Color c) {
    if (x < 0 || x >= (int32_t)FB_WIDTH || y < 0 || y >= (int32_t)FB_HEIGHT) return;
    buffer_[pixel_index(x, y)] = (c.a << 24) | (c.r << 16) | (c.g << 8) | c.b;
    dirty_ = true;
}

Color Framebuffer::get_pixel(int32_t x, int32_t y) const {
    if (x < 0 || x >= (int32_t)FB_WIDTH || y < 0 || y >= (int32_t)FB_HEIGHT) return Color::Black();
    uint32_t p = buffer_[pixel_index(x, y)];
    return Color((p >> 16) & 0xFF, (p >> 8) & 0xFF, p & 0xFF, (p >> 24) & 0xFF);
}

void Framebuffer::fill_rect(int32_t x, int32_t y, uint32_t w, uint32_t h, Color c) {
    if (x < 0) { if ((uint32_t)(-x) >= w) return; w -= (-x); x = 0; }
    if (y < 0) { if ((uint32_t)(-y) >= h) return; h -= (-y); y = 0; }
    if (x + (int32_t)w > (int32_t)FB_WIDTH) w = FB_WIDTH - x;
    if (y + (int32_t)h > (int32_t)FB_HEIGHT) h = FB_HEIGHT - y;
    if (w == 0 || h == 0) return;
    
    uint32_t pixel = (c.a << 24) | (c.r << 16) | (c.g << 8) | c.b;
    for (uint32_t row = 0; row < h; ++row) {
        uint32_t* row_ptr = &buffer_[(y + row) * FB_WIDTH + x];
        for (uint32_t col = 0; col < w; ++col) {
            row_ptr[col] = pixel;
        }
    }
    dirty_ = true;
}

void Framebuffer::draw_rect(int32_t x, int32_t y, uint32_t w, uint32_t h, Color c, uint32_t line_width) {
    // Top
    fill_rect(x, y, w, line_width, c);
    // Bottom
    fill_rect(x, y + h - line_width, w, line_width, c);
    // Left
    fill_rect(x, y, line_width, h, c);
    // Right
    fill_rect(x + w - line_width, y, line_width, h, c);
}

void Framebuffer::draw_line(int32_t x0, int32_t y0, int32_t x1, int32_t y1, Color c) {
    int32_t dx = (x1 >= x0) ? (x1 - x0) : (x0 - x1);
    int32_t dy = (y1 >= y0) ? (y1 - y0) : (y0 - y1);
    int32_t sx = (x0 < x1) ? 1 : -1;
    int32_t sy = (y0 < y1) ? 1 : -1;
    int32_t err = dx - dy;
    
    while (true) {
        set_pixel(x0, y0, c);
        if (x0 == x1 && y0 == y1) break;
        int32_t e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 < dx) { err += dx; y0 += sy; }
    }
}

void Framebuffer::draw_circle(int32_t cx, int32_t cy, int32_t radius, Color c, bool filled) {
    int32_t x = radius;
    int32_t y = 0;
    int32_t err = 0;
    
    while (x >= y) {
        if (filled) {
            draw_line(cx - x, cy + y, cx + x, cy + y, c);
            draw_line(cx - y, cy + x, cx + y, cy + x, c);
            draw_line(cx - x, cy - y, cx + x, cy - y, c);
            draw_line(cx - y, cy - x, cx + y, cy - x, c);
        } else {
            set_pixel(cx + x, cy + y, c);
            set_pixel(cx + y, cy + x, c);
            set_pixel(cx - x, cy + y, c);
            set_pixel(cx - y, cy + x, c);
            set_pixel(cx + x, cy - y, c);
            set_pixel(cx + y, cy - x, c);
            set_pixel(cx - x, cy - y, c);
            set_pixel(cx - y, cy - x, c);
        }
        
        y++;
        err += 1 + 2 * y;
        if (2 * (err - x) + 1 > 0) {
            x--;
            err += 1 + 2 * (x + 1);
        }
    }
}

uint32_t Framebuffer::draw_text(int32_t x, int32_t y, const char* text, Color fg, Color bg) {
    if (!text) return 0;
    
    uint32_t start_x = x;
    while (*text) {
        uint8_t ch = *text;
        if (ch < 32 || ch > 126) {
            ++text;
            continue;
        }
        
        const uint8_t* glyph = font_8x16[ch - 32];
        
        for (int row = 0; row < 16; ++row) {
            uint8_t bits = glyph[row];
            for (int col = 0; col < 8; ++col) {
                if (bits & (0x80 >> col)) {
                    set_pixel(x + col, y + row, fg);
                } else {
                    set_pixel(x + col, y + row, bg);
                }
            }
        }
        
        x += 8;
        ++text;
    }
    
    return x - start_x;
}

uint32_t Framebuffer::draw_text_scaled(int32_t x, int32_t y, const char* text,
                                       Color fg, Color bg, uint32_t scale) {
    if (!text) return 0;
    if (scale <= 1) return draw_text(x, y, text, fg, bg);

    const uint32_t start_x = static_cast<uint32_t>(x);
    while (*text) {
        uint8_t ch = *text;
        if (ch < 32 || ch > 126) {
            ++text;
            continue;
        }
        const uint8_t* glyph = font_8x16[ch - 32];
        for (int row = 0; row < 16; ++row) {
            uint8_t bits = glyph[row];
            for (int col = 0; col < 8; ++col) {
                const Color c = (bits & (0x80 >> col)) ? fg : bg;
                fill_rect(x + col * static_cast<int32_t>(scale),
                          y + row * static_cast<int32_t>(scale),
                          scale, scale, c);
            }
        }
        x += 8 * static_cast<int32_t>(scale);
        ++text;
    }
    return static_cast<uint32_t>(x) - start_x;
}

void Framebuffer::blit(int32_t dx, int32_t dy, const uint32_t* src, uint32_t sw, uint32_t sh, uint32_t src_stride) {
    for (uint32_t row = 0; row < sh; ++row) {
        int32_t dest_y = dy + row;
        if (dest_y < 0 || dest_y >= (int32_t)FB_HEIGHT) continue;
        
        for (uint32_t col = 0; col < sw; ++col) {
            int32_t dest_x = dx + col;
            if (dest_x < 0 || dest_x >= (int32_t)FB_WIDTH) continue;
            
            uint32_t src_idx = row * src_stride + col;
            buffer_[dest_y * FB_WIDTH + dest_x] = src[src_idx];
        }
    }
    dirty_ = true;
}

void Framebuffer::screenshot(uint32_t* out_buffer) const {
    if (!out_buffer || !buffer_) return;
    for (size_t i = 0; i < FB_WIDTH * FB_HEIGHT; ++i) {
        out_buffer[i] = buffer_[i];
    }
}

